package co.viajesglobal.MicroservicePagos.Enum;

/**
 * Enum que define los posibles medios de pago utilizados en el sistema.
 * Esta enumeración representa los tipos de tarjetas que pueden ser utilizadas para realizar pagos.
 * Los valores disponibles son:
 * - Tarjeta de Crédito
 * - Tarjeta de Débito
 */
public enum MedioPago {
    // Representa un pago realizado con una tarjeta de crédito
    Tarjeta_de_Credito, 
    
    // Representa un pago realizado con una tarjeta de débito
    Tarjeta_de_Debito
}
